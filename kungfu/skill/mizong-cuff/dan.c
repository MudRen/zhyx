// dan.c 腰斩

#include <ansi.h>
#include <combat.h>

inherit F_SSERVER;

int perform(object me, object target)
{
        object weapon,weapon2;
        int damage,i;
        string msg,str;
        int ap, dp;

        if (! target)
        {
                me->clean_up_enemy();
                target = me->select_opponent();
        }
        
        if (! target || ! me->is_fighting(target))
                return notify_fail("「腰斩」只能对战斗中的对手使用。\n");
 
        if(me->query_skill_mapped("cuff") != "mizong-cuff") 
                return notify_fail("你没有用迷踪拳，无法使用「腰斩」绝招！\n");

        if (me->query_skill_prepared("cuff") != "mizong-cuff")
                return notify_fail("你没有准备使用迷踪拳，无法施展「腰斩」绝招。\n");

        if (objectp(weapon = me->query_temp("weapon")) ||
            objectp(weapon = me->query_temp("handing")) )
                return notify_fail("你必须空着双手才能使用掌法绝招。\n");

        if ((int)me->query_skill("mizong-cuff", 1) < 200)
                return notify_fail("你的迷踪拳不够娴熟，不会使用「腰斩」。\n");
                                
        if ((int)me->query_skill("force") < 300)
                return notify_fail("你的内功修为不够高。\n");
                        
        if( (int)me->query("neili") < 800 )
                return notify_fail("你现在内力太弱，不能使用「腰斩」。\n");
                        
        msg = HIR "\n$N" HIR "突然绕到$n" HIR "身后，双拳猛的击向$n" HIR "的后腰！\n" NOR;

        ap = me->query_skill("cuff") + me->query_skill("force");
        ap += me->query("jiali");
        ap += me->query("jianu");
        
        damage = ap*2 + random(ap);
        damage += me->query("jiali");

        if (living(target))
                dp = target->query_skill("dodge",1) + target->query_skill("parry",1);
        else
                dp = 0;

        dp = dp*3/2;

        weapon2 = target->query_temp("weapon");
        
        if (ap / 2 + random(ap) > dp)
        {
                me->add("neili", -damage/2);

                msg += HIR "只听“咔嚓”一声闷响！$n" HIR "喷出一口鲜血，软塌塌的倒了下去！\n" NOR;
                target->receive_damage("qi",damage,me);
                target->receive_wound("qi",damage/2 + random(damage/2),me);
		str = COMBAT_D->status_msg((int)target->query("qi") * 100 /(int)target->query("max_qi"));
		msg += "($n"+str+")\n";
                
                target->start_busy(1+random(2));
                me->start_busy(3+random(2));
        } else 
        {
                msg += HIB "哪知$n" HIB "早就有所准备，身形一展，轻易的化解了这招！\n" NOR;

                me->add("neili", -100);
                me->start_busy(3);
                if (target->is_busy())
                        target->start_busy(1);
        }
        message_combatd(msg, me, target);

        return 1;
}
